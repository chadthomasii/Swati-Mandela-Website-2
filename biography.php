            <p>
                Swati, grew up in a world of contrasts. Even as her grandfather was becoming an iconic, 
                revered figure throughout the world, in her native land, the media and post-Apartheid patriarchal 
                system were vilifying her grandmother. Aware of the horrors her grandfather had endured, 
                while at the same time observing the scorn heaped upon her grandmother, she began to understand the 
                role the media played in shaping opinion and both correcting and enabling injustice. She went on to earn 
                her bachelor’s degree in Applied Public Relations from Midrand Graduate Institute in 2002. 
                She worked for several communications firms, including Blackrock and Tincan, and in the press office 
                of prestigious fashion designer House of Busby, before taking a position as spokesperson and chief 
                fundraiser for 21 Icons. Among her successful projects for the firm were lead roles in producing documentaries 
                on South Africa’s three Nobel Peace Prize winners: Desmond Tutu, F.W. de Klerk and, of course, Nelson Mandela.
            </p>

            <br>

            <p>
                Since 2015 she has been the managing director of Qunu Workforce, 
                whose mandate is to create equality in the workplace for those living with disabilities. 
                She is also a director of Long Walk To Freedom (LWTF), a non-profit that provides libraries 
                for disadvantaged schools.
            </p>

            <br>

            <p>
                She is co-author of “491 Days,” a book written by her grandmother that details the brutality 
                she received living under an Apartheid state. She also wrote the foreword to the book, 
                “The Prison Letters of Nelson Mandela” (Published 2018), a collection of Nelson Mandela’s prison 
                letters spanning the 27 years he was held as a political prisoner. Swati is proud to serve as a Global 
                Ambassador/Mentor for AIME, an organization dedicated to building a “World Of Mentoring”, a bridge 
                between universities and high schools.
            </p>

